-- 清理旧表
DROP TABLE IF EXISTS t_aftersale_ticket;
DROP TABLE IF EXISTS t_logistics;
DROP TABLE IF EXISTS t_order;
DROP TABLE IF EXISTS t_goods;
DROP TABLE IF EXISTS t_customer;

-- 1. 创建客户表
CREATE TABLE t_customer (
    id INT PRIMARY KEY AUTO_INCREMENT,
    user_id VARCHAR(50) NOT NULL,
    member_level VARCHAR(50) NOT NULL,
    total_order_count INT NOT NULL,
    has_unfinished_aftersale TINYINT(1) NOT NULL,
    points INT NOT NULL,
    register_time DATETIME NOT NULL,
    phone VARCHAR(20) NOT NULL
);

-- 2. 创建商品表
CREATE TABLE t_goods (
    goods_id VARCHAR(20) PRIMARY KEY,
    goods_name VARCHAR(100) NOT NULL,
    price DECIMAL(10, 2) NOT NULL,
    origin_price DECIMAL(10, 2) NOT NULL,
    spec TEXT NOT NULL,
    stock INT NOT NULL,
    `desc` TEXT NOT NULL
);

-- 3. 创建订单表
CREATE TABLE t_order (
    id INT PRIMARY KEY AUTO_INCREMENT,
    order_no VARCHAR(50) NOT NULL,
    user_id VARCHAR(20) NOT NULL,
    goods_name VARCHAR(100) NOT NULL,
    pay_amount DECIMAL(10, 2) NOT NULL,
    order_status VARCHAR(20) NOT NULL,
    create_time DATETIME NOT NULL
);

-- 4. 创建物流表
CREATE TABLE t_logistics (
    id INT PRIMARY KEY AUTO_INCREMENT,
    tracking_no VARCHAR(50) NOT NULL,
    track_list JSON NOT NULL
);

-- 5. 创建售后工单表
CREATE TABLE t_aftersale_ticket (
    id INT PRIMARY KEY AUTO_INCREMENT,
    user_id VARCHAR(20) NOT NULL,
    session_id VARCHAR(50) NOT NULL,
    order_no VARCHAR(50) NOT NULL,
    problem_desc TEXT NOT NULL,
    create_time DATETIME NOT NULL,
    update_time DATETIME NOT NULL,
    ticket_status VARCHAR(20) NOT NULL
);

